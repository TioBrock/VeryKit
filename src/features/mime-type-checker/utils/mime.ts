export function getMimeType(ext: string): string | null {
  let normalized = ext.trim().toLowerCase();
  if (!normalized.startsWith(".")) {
    normalized = `.${normalized}`;
  }
  return mimeTypes[normalized] ?? null;
}

export function searchMimeTypes(query: string): Array<{ extension: string; mimeType: string }> {
  const q = query.toLowerCase();
  return Object.entries(mimeTypes)
    .filter(([ext, mime]) => ext.includes(q) || mime.includes(q))
    .map(([ext, mime]) => ({ extension: ext, mimeType: mime }));
}
